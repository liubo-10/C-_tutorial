void CTime_CounterDlg::OnTimer(UINT_PTR nIDEvent)
{
	// TODO: 在此添加消息处理程序代码和/或调用默认值
	static UINT mm = 0;
	static UINT ss = 0;
	static UINT mss = 0;
	CString str;
	switch (nIDEvent)
	{
	  case 1:
		   mss++;
		   if (mss == 100)
		   {
			  mss = 0;
			  ss++;
		   }
		   if(ss == 60)
		   {
             ss = 0;
			 mm++;
		   }
		   str.Format(_T("%02i:%02i:%02i"),mm,ss,mss);
		   m_time.SetWindowTextW(str);
		   break;
	 }
	CDialogEx::OnTimer(nIDEvent);
}
